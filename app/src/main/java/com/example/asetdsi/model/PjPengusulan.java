package com.example.asetdsi.model;

public class PjPengusulan {
    public String nama_pj_pengusulan;
    public Integer jumlahpj_pengusulan;
    public Integer id;
    public PjPengusulan(){}


    public PjPengusulan(String nama_pj_pengusulan, Integer jumlahpj_pengusulan, Integer id) {
        this.nama_pj_pengusulan = nama_pj_pengusulan;
        this.jumlahpj_pengusulan = jumlahpj_pengusulan;
        this.id = id;
    }
}
